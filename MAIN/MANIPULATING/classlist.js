// check class list 
let obj = document.querySelector("h1");
console.log(obj.classList);
// add class list 
console.log(obj.classList.add("addclass"));
// remove a class list
console.log(obj.classList.remove("addclass"));
// check if class exists 
console.log(obj.classList.contains("addclass"));
// toggle  off  & on 
console.log(obj.classList.toggle("addclass"));